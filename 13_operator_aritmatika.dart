void main() {
  var operand1 = 10;
  var operand2 = 3;
  var a = operand1 + operand2;
  print("(Pertambahan), $operand1\+$operand2=$a");
  var b = operand1 - operand2;
  print("(Pengurangan), $operand1\-$operand2=$b");

  print("(Perkalian), $operand1\*$operand2");

  print("(Pembagian (double)), $operand1\~/$operand2");

  print("(Pembagian (int)), $operand1\/$operand2");

  print("(Sisa Bagi), $operand1\%$operand2");
}
